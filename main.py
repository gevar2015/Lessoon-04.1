b = 10  # Присваиваем b значение 10

# Проверяем условие, если a больше b
if a > b:
    print("Сложение:", a + b)  # Вывод результата сложения
    print("Вычитание:", a - b)  # Вывод результата вычитания
if a < b:
    print("Сложение:", a + b)  # Вывод результата сложения
    print("Вычитание:", b - a)  # Вывод результата вычитания
else:
    print("Равенство а и b")
    